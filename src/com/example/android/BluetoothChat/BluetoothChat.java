	/**
	 * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
	 * one of the sections/tabs/pages.
	 */
	public class SectionsPagerAdapter extends FragmentPagerAdapter {

		private Fragment ecgFrag;
		private Fragment historyFrag;
		public SectionsPagerAdapter(FragmentManager fm) {
			super(fm);
			ecgFrag = new ECG();
			Bundle args = new Bundle();
			args.putInt(ECG.ARG_SECTION_NUMBER, 1);
			ecgFrag.setArguments(args);
			historyFrag = new History();
			Bundle args2 = new Bundle();
			args2.putInt(History.ARG_SECTION_NUMBER, 2);
			ecgFrag.setArguments(args2);
		}

		@Override
		public Fragment getItem(int position) {
			// getItem is called to instantiate the fragment for the given page.
			if (position == 0) {
				return ecgFrag;
			}
			else {
				return historyFrag;
			}
		}

		@Override
		public int getCount() {
			// Show 2 total pages.
			return 2;
		}

		@Override
		public CharSequence getPageTitle(int position) {
			Locale l = Locale.getDefault();
			switch (position) {
			case 0:
				//return getString(R.string.title_section1).toUpperCase(l);
				CharSequence ecg = "ECG Graph";
				return ecg;
			case 1:
				//return getString(R.string.title_section2).toUpperCase(l);
				CharSequence stHistory = "ST Seg History";
				return stHistory;
			}
			return null;
		}
	}

